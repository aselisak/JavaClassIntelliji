public class Replit69 {
    public static void main(String[] args) {
        //Write a program to print out the pattern:

        for (int i=1; i<=7; i++){
            for (int x=1; x<=7-i; x++){
                System.out.print(x+" ");
            }
            System.out.println();

        }for (int i=2; i<=7; i++){
            for (int x=1; x<=i; x++){
                System.out.print(x+" ");
            }
            System.out.println();
        }



    }
}
